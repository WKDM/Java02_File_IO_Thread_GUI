public class Direction3 {
	public static final Direction3 AFTER = new Direction3("后"){
		public void show(){
			System.out.println("后");
		}
	};
	public static final Direction3 BEFTER = new Direction3("前"){
		public void show(){
			System.out.println("前");
		}
	};
	public static final Direction3 LEFT = new Direction3("左"){
		public void show(){
			System.out.println("左");
		}
	};
	public static final Direction3 RIGHT = new Direction3("右"){
		public void show(){
			System.out.println("右");
		}
	};
	private String name;
	private Direction3(String name){
		this.name = name;
	}
	public String getName(){
		return name;
	}
	public void show(){
		System.out.println(getName());
	}
}